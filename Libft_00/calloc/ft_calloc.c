#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*p;

	if (nmemb == 0 || size == 0)
		return (p);
	p = malloc(nmemb * size);
	ft_bzero(p, nmemb * size);
	return (p);
}
